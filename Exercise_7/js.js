
let colors = ["red", "green", "blue"];
console.log("Colors in the array:");
console.log(colors); // ["red", "green", "blue"]

//add an element to the end of the array
colors.push("yellow"); // adds "yellow" to the end of the array
console.log("Colors in the array after adding an element to the end:");
console.log(colors); // ["red", "green", "blue", "yellow"]

// Remove the first element from the array
colors.shift(); // removes the first element ("red")
console.log("Colors in the array after removing the first element:");
console.log(colors); // ["green", "blue"]